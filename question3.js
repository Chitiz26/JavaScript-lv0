let b=true;
console.log(a + 10); // This will concatenate the string "5" with the number 10, resulting in "510"
console.log(a - 10); // This will convert the string "5" to a number and perform subtraction, resulting in -5
console.log(a * 10); // This will convert the string "5" to a number and perform multiplication, resulting in 50
console.log(a / 10); // This will convert the string "5" to a number and perform division, resulting in 0.5
